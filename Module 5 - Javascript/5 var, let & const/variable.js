// var vs let

// 1. var is function scoped
// 2. let is block scoped
// 3. var can be redeclared
// 4. let cannot be redeclared
// 5. var can be updated
// 6. let can be updated
// 7. var is hoisted
// 8. let is not hoisted

// const vs let

// 1. const is block scoped
// 2. let is block scoped
// 3. const cannot be redeclared
// 4. let cannot be redeclared
// 5. const cannot be updated
// 6. let can be updated
// 7. const is hoisted
// 8. let is not hoisted
