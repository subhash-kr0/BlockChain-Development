// Arithmetic operators
// + - * / % ++ --

var a = 10;
var b = 20;
var c = 30;
var d = 40;
var e = 50;
var f = 60;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

console.log(a++);
console.log(a);
console.log(++a);
console.log(a);

console.log(b--);
console.log(b);
console.log(--b);
console.log(b);

console.log(c += d);
console.log(c -= d);
console.log(c *= d);
console.log(c /= d);
console.log(c %= d);

console.log(e ** f);
console.log(Math.pow(e, f));
