// Comparision Operator

let x = 10;
let y = 20;

console.log(x == y);
console.log(x != y);
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);

console.log(x == "10");
console.log(x === "10");
console.log(x === 10);
console.log(x !== "10");
console.log(x !== 10);

console.log(x == true);
console.log(x == false);
console.log(x === true);
console.log(x === false);
console.log(x != true);
console.log(x != false);
console.log(x !== true);
console.log(x !== false);

console.log(x == null);
console.log(x == undefined);
console.log(x === null);
console.log(x === undefined);
console.log(x != null);
console.log(x != undefined);
console.log(x !== null);
console.log(x !== undefined);
