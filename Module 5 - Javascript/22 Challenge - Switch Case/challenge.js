// Challenge in switch case

//0 ==monday
//1 ==tuesday
//2 ==wednesday
//3 ==thursday
//4 ==friday
//5 ==saturday
//6 ==sunday

let day = 0;

switch (day) {
    case 0:
        console.log('monday');
        break;
    case 1:
        console.log('tuesday');
        break;
    case 2:
        console.log('wednesday');
        break;
    case 3:
        console.log('thursday');
        break;
    case 4:
        console.log('friday');
        break;
    case 5:
        console.log('saturday');
        break;
    case 6:
        console.log('sunday');
        break;
    default:
        console.log('invalid day');
        break;
}
