// Logical NOT, OR and AND

// Logical NOT (!)
// The logical NOT operator is written as !.
// It is used to reverse the logical state of its operand.
// If a condition is true, the logical NOT operator will make it false.
// Example
let x = 10;
console.log(x);
console.log(!x);
console.log(!!x);
console.log(!!!x);
console.log(!!!!x);
console.log(!!!!!x);
console.log(!!!!!!x);
console.log(!!!!!!!x);

// Logical OR (||)
// The logical OR operator is written as ||.
// It is used to compare two conditions.
// If any one of the condition is true, the logical OR operator will make the whole condition true.

// Example
let a = 10;
let b = 20;

console.log(a == 10 || b == 20);

// Logical AND (&&)
// The logical AND operator is written as &&.
// It is used to compare two conditions.
// If both the conditions are true, the logical AND operator will make the whole condition true.

// Example

let c = 10;
let d = 20;

console.log(c == 10 && d == 20);
